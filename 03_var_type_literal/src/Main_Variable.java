public class Main_Variable {
    public static void main(String[] args) {
        // 変数がなかったら…
        System.out.println(10 + 5);
        System.out.println(10 - 3);

        // 変数があると…
        int x = 10;
        int answer1 = x + 5;
        int answer2 = x - 3;
        System.out.println(answer1);
        System.out.println(answer2);
    }
}